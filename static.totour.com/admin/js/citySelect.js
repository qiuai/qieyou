var provSelect = $('#province');
var citySelect = $('#city');
var destSelect = $('#dest');
var localSelect = $('#local');
var innSelect = $('#inn');

$.initLocalSelect = function(defaultProv,defaultCity) {
	var show_default = false;
	if(!defaultProv&&!defaultCity)
	{
		var show_default = true;
	}
	defaultProv = defaultProv?defaultProv:'530000';
	defaultCity = defaultCity?defaultCity:'530700';
    var ProvRow = getProvRow(defaultProv);
    var provHtml = '';
    provHtml += '<option ref="-1" value="">请选择</option>';
    for(var i = 0; i < $._cityInfo.length; i++) {
        provHtml += '<option ref="' + i + '" value="' + $._cityInfo[i].n + '"' + ( $._cityInfo[i].nid == defaultProv ? ' selected="selected"' : '') + '>' + $._cityInfo[i].n + '</option>';
    }
    provSelect.html(provHtml);
    $.initCities(ProvRow, defaultCity);
	if(show_default)
	{
		citySelect.change();
	}
    provSelect.change(function() {
        var currId = parseInt(provSelect.find('option:selected').attr("ref"));
        $.initCities(currId);
		
		destSelect.empty();
		destSelect.append('<option value="0">所有区域</option>');
		localSelect.empty();
		localSelect.append('<option value="0">所有街道</option>');
		innSelect.empty();
		innSelect.append('<option value="0">所有商户</option>');
    });
};

$.initCities = function(provId, defaultCity) {
    var hasDefaultCity = (typeof(defaultCity) != 'undefined');
    if(provSelect.val() != '' && provId >= 0) {
        var cities = $._cityInfo[provId].c;
        var citieIds = $._cityInfo[provId].pid;
        var cityHtml = '';

        cityHtml += '<option value="">请选择</option>';
		if(cities.length>0)
		{
			for(var i = 0; i < cities.length; i++) {
				cityHtml += '<option value="' + citieIds[i] + '"' + ((hasDefaultCity && citieIds[i] == defaultCity) ? ' selected="selected"' : '') + '>' + cities[i] + '</option>';
			}
			citySelect.html(cityHtml);
		}
		else{
			citySelect.html('<option value="">尚未新建</option>');
		}
		
    } else {
        citySelect.html('<option value="">请先选择</option>');
    }
};

function getProvRow(province){
    var provinceId = '';
    for(var i = 0; i < $._cityInfo.length; i++){
        if($._cityInfo[i].nid == province ){
            provinceId = i;
            break;
        }
    }
    return provinceId;
}

citySelect.change(function(){
	city_value = $('#city').val();
	if(city_value <0)
	{
		return false;
	}
	$.ajax({
		url: baseUrl+'destination/getDestinations',
		type:'POST',
		data:{city_id:$('#city').val()},
		dataType: 'json',
		success: function(data){
			if(data == ''){
				destSelect.empty();
				destSelect.append('<option value="-1">该地区暂无登记</option>');
			}
			else{
				destSelect.empty();
				destSelect.append('<option value="0">所有区域</option>');
				for(var i=0;i<data.length;i++){

					destSelect.append('<option value="'+data[i].dest_id+'">'+data[i].dest_name+'</option>');
				}
			}
			localSelect.empty();
			localSelect.append('<option value="0">所有街道</option>');
			innSelect.empty();
			innSelect.append('<option value="0">所有商户</option>');
		}
	});
});